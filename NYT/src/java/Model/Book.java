package Model;

/**
 *
 * @author MyCom
 */
public class Book {
    int ID;
    String title;
    String author;

    public Book(int ID, String title, String author) {
        this.ID = ID;
        this.title = title;
        this.author = author;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }
    
    
}
